# 13. Пользователь задаёт две строки. Определить количество символов одной строки,
# входящих в другую.

str_1 = str(input ('Введите строку 1: '))
str_2 = str(input ('Введите строку 2: '))

set_1 = set(str_1)
set_2 = set(str_2)

rez = set_1.intersection(set_2)
print ('Количество вхождений:', len(rez))